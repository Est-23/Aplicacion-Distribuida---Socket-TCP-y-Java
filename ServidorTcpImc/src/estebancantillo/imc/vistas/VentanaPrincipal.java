    ///Este metodo maneja el evento clic del botón "Iniciar/Detener"
    private ServidorTcp s;
    private void BtnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIniciarActionPerformed

        if(BtnIniciar.getText().equalsIgnoreCase("INICIAR")){ //Si el botón está en modo "INICIAR", crea un objeto "ServidorTcp" con el puerto especificado, lo inicia y actualiza la interfaz con el estado y mensajes del servidor.
           int puerto = Integer.parseInt(campoPuerto.getText());
           s = new ServidorTcp(puerto, this);
           s.start();
           BtnIniciar.setText("Detener");
           txtEstado.setText("ONLINE");
           txtEstado.setForeground(Color.GREEN);
        }else if (BtnIniciar.getText().equalsIgnoreCase("DETENER")){ //Si el botón está en modo "Detener", detiene el servicio del servidor y actualiza la interfaz con el estado y mensajes del servidor.
            s.detenerServicio();
            BtnIniciar.setText("INICIAR");
            txtEstado.setText("OFF LINE");
            txtEstado.setForeground(Color.red);
        }
    }//GEN-LAST:event_BtnIniciarActionPerformed

