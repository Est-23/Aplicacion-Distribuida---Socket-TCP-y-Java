    /*
    *Este metodo Se ejecuta al abrir la ventana.
    *Primero deshabilita la edición del campo de texto de la IP
    *y luegobtiene la dirección IP de la máquina local y la muestra en el campo de texto.
    */
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        String ip;
        try {
            campoIP.setEditable(false);
            ip = InetAddress.getLocalHost().getHostAddress();
            campoIP.setText(ip);
        } catch (UnknownHostException e) {
            JOptionPane.showMessageDialog(this, "Falla en la conexión");
        }
    }//GEN-LAST:event_formWindowOpened

